// Initialize a counter
let counter = 1;

// Loop while counter is less than or equal to 5
while (counter <= 5) {
    console.log(`while loop iteration: ${counter}`);
    counter++;
}

//==========================================================================

// Initialize a counter
let doCounter = 1;

// Loop while doCounter is less than or equal to 5
do {
    console.log(`do/while loop iteration: ${doCounter}`);
    doCounter++;
} while (doCounter <= 5);
